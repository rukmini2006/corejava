package rukmini.tutorial.collections;

public class Person {
String name;
String Profession;

public Person(){
	
}

public String getName() {
	return name;
}

public void setName(String name) {
	this.name = name;
}

public String getProfession() {
	return Profession;
}

public void setProfession(String profession) {
	Profession = profession;
}
}
