
public class MathClass {

	public static void main(String[] args) {
		//E and Round()
		System.out.println("e =  "+Math.E);
		System.out.println("round  "+Math.round(100));
       //abs()
		System.out.println(Math.abs(Math.PI));
		//ceil
		System.out.println(" Smallest Number but greateer than the argument " +Math.ceil(Math.PI));
		//exp()
		System.out.println(+Math.exp(2));
		//floor()
		System.out.println("Largest number but less than yhe argument:  "+Math.floor(Math.PI));
		//max()
		System.out.println(" Maximum number is "+Math.max(10, 10.3));
		//min()
		System.out.println(" Minimum number is : "+Math.min(10, 4));
		//pow()
		System.out.println("Power is:  "+Math.pow(2,3));
		//random()
		System.out.println(" Random number is : " +Math.random());
		//rint()
		System.out.println(" closest to the argument "+Math.rint(39));
		//round()
		System.out.println(" Round is "+Math.round(Math.E));
		//sqrt()
		System.out.println("Square root is : "+Math.sqrt(400));
		
	}

}
